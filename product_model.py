import streamlit as st

# Заголовок программы
st.title("Экспертная система: Прогноз курса валют")
st.subheader("Предметная область: Финансовая аналитика")

# Блок ввода данных (фактов)
st.sidebar.header("Экономические показатели")
oil_price = st.sidebar.select_slider(
    "Цена на нефть (Brent)", options=["Низкая", "Средняя", "Высокая"]
)
inflation = st.sidebar.select_slider("Уровень инфляции", options=["Низкий", "Высокий"])
rate_change = st.sidebar.selectbox(
    "Решение Центробанка по ставке",
    ["Повышение", "Оставили без изменений", "Понижение"],
)

# Кнопка запуска логического вывода
if st.button("Выполнить анализ"):

    st.write("### Результат анализа:")

    # Продукционная модель (Правила ЕСЛИ-ТО)
    # Правило 1
    if oil_price == "Высокая" and rate_change == "Повышение":
        st.success("ПРОГНОЗ: Укрепление национальной валюты.")
        st.write("Рекомендация: Продавать валюту, покупать облигации.")

    # Правило 2
    elif oil_price == "Низкая" or inflation == "Высокий":
        st.error("ПРОГНОЗ: Ослабление валюты (девальвация).")
        st.write("Рекомендация: Покупать валюту или защитные активы (золото).")

    # Правило 3
    elif rate_change == "Понижение" and oil_price == "Средняя":
        st.warning("ПРОГНОЗ: Умеренное снижение курса.")
        st.write("Рекомендация: Держать текущие позиции.")

    # Правило по умолчанию
    else:
        st.info("ПРОГНОЗ: Стабильный курс.")
        st.write("Рекомендация: Особых действий не требуется.")

st.info("Данная система работает на основе заложенных правил (продукционная модель).")
